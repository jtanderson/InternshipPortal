      add_info.value = l.additional_info;
      duration.value = l.duration;
      app_open.value = formatDate(l.app_open);
      app_close.value = formatDate(l.app_close);
    });

    // TODO: TEST THIS THOROUGHLY!!!
    // MAKE SURE TO CATCH EDGE CASES
